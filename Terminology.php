                            <h5 class="text-info">Water Resources</h5>
                            <p class="font-family text-justify">
                                Water available, or capable of being made available, for use in sufficient quantity and quality at a location and over a period of time appropriate for an identifiable demand.
                            </p>
                            <hr class="glossary-hr" />
                            <h5 class="text-info">Water Resources Assessment</h5>
                            <p class="font-family text-justify">
                                Determination of the sources, extent, dependability and quality of water resources for their utilisation and control.
                            </p>
                            <hr class="glossary-hr" />
                            <h5 class="text-info">Catchment Area</h5>
                            <p class="font-family text-justify">A river's catchment (or basin) is the land area from which rainfall will ultimately contribute to the river discharge. The catchment area of the Ganges, Brahmaputra and Meghna Rivers are 907 X 103 km2, 583 X 103 km2 and 65 X 103 km2, respectively, of which only 8% lies in Bangladesh. More than 90% of the water that flows into the Bay of Bengal enters Bangladesh through its borders with India.</p>
                            <hr class="glossary-hr" />
                            <h5 class="text-info">Supper Model</h5>
                            <p class="font-family text-justify">FFWC simulates the water level conditions in Bangladesh by use of a numerical model of the Bangladeshi river network. The model is named the "Super Model" and is based on measurements of the topography of Bangladesh and the general numerical hydrodynamic model system named MIKE 11 developed at DHI Water & Environment. To run the Super Model information on water level and rainfall is needed to force the model: water level boundaries are e.g. found upstream in Ganges and Brahmaputra and downstream in Meghna River. The Super Model has been run at the FFWC since 1998.</p>
                            <hr class="glossary-hr" />
                            <h5 class="text-info">Water cycle</h5>
                            <p class="font-family text-justify">
                                Water is the lifeblood of the weather. It moves into the atmosphere as water vapour through evaporation from water surfaces or through transpiration from plants. The vapour condenses in the atmosphere to form clouds and returns to the surface as precipitation (rain, snow or hail) and in small quantities as fog, dew or frost.

                                Water falling on land is destined for one of many fates. If it falls as snow it can spend some time in a snow or ice-field before moving off as a glacier and perhaps eventually becoming part of an iceberg. Or it might melt and join a river. If it falls as liquid drops it might be retained in the soil as moisture for plants, or it might join a river, or be stored for a time in a lake or reservoir.

                                Ultimately rain or snow falling over land will either find its way back to the ocean or be evaporated back into the atmosphere, from where it will move along through various branches of the eternal water (hydrologic) cycle.
                            </p>
