                            <h4 class="text-primary">Flood Forecasting and Warning Service</h4>
                            <br />
                            <h5 class="text-info">Background</h5>
                            <p class="font-family text-justify">
                                Bangladesh is one of the most flood prone countries in the world. Due to its location in the low-lying deltaic floodplains at the convergence of three Himalayan Rivers: Ganges, Brahmaputra and Meghna (GBM), heavy monsoon rainfall concomitant with poor drainage often results in annual flooding. These river systems drain a catchment area of about 1.662 million sq. km. The floodplains of the rivers are home to a large population, most of which is rural and poor, and whose life is intricately linked to the flooding regime. Recurrent floods cause widespread damage in rural and urban areas and set back the country's efforts to alleviate poverty.

                                Huge cross-border inflows, low-line topography, deposition of sediment in the rivers, infrastructure development in floodplains have made the country highly vulnerable to flood. As a deltaic land, the ecosystem of the country is water based, and flood is a part of life of the countryís environment. No disagreement that recurrent high floods damage the economy and causes tremendous sufferings to the lives. The reality is that flood brings sufferings to lives for very short time but absence of flood will cause sufferings for much longer. The country needs floods along with the flood coping strategies and flood management provision to reduce damages and sufferings. Flood forecasting is a very effective flood management activity that is being extensively applied in all over the world. In Bangladesh, flood forecasting was started in 1972 through establishment of Flood Forecasting and Warning Centre (FFWC) under the Bangladesh Water Development Board (BWDB). Institute of Water Modelling (IWM) frequently provides technical support to FFWC for smooth operation and updating of the Flood Forecasting and Warning System. This site describes upon the updates of our endeavor on research in the field of quantitative flood forecasting and warning services.
                            </p>
                            <hr class="glossary-hr" />
                            <h5 class="text-info">Objectives</h5>
                            <p class="font-family text-justify">
                                The prime objective of this research is to devise a high performance flood forecasting system with least expenses of resources and at the earliest of the day. The specific objectives are:
                                <ul>
                                    <li>Quantitative Boundary Flow Prediction (QBFP);</li>
                                    <li>Increase lead time of boundary prediction;</li>
                                    <li> Increase lead time of flood forecast inside the country;</li>
                                    <li>Diversify forecast outputs for categories of end users;</li>
                                    <li>Extend flood forecasting to the entire GBM basin and even others;</li>
                                </ul>
                            </p>
                            <hr class="glossary-hr" />
                            <h5 class="text-info">Approach</h5>
                            <p class="font-family text-justify">
                                This flood forecasting system is completely numerical model based. Uses several models: Weather Research and Forecast (WRF) model, hydrological model (MIKE 11 NAM & MIKEBASIN of Danish Hydraulic Institute), and hydrodynamic model (MIKE 11 HD of Danish Hydraulic Institute). Now cast is generated using real time precipitation data where as forecast is generated using precipitation prediction.
                            </p>
                            <hr class="glossary-hr" />
                            <h5 class="text-info">Methodologies</h5>
                            <p class="font-family text-justify">
                                Under the system, cross boundary inflows into the country are predicted through simulation of the GBM model. The GBM basin model is simulated for 1 year hindcast and 6 days forecast period using real time rainfall and predicted rainfall, respectively. Using the predicted cross border inflows and predicted rainfall, the Flood Forecasting Super Model is simulated within the country for generating flood forecast with lead time of 5 days at 54 stations of major rivers.
                            </p>
