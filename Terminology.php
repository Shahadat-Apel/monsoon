                            <h5 class="text-info">Precipitation</h5>
                            <p class="font-family text-justify">Any or all of the forms of water, whether liquid (e.g. rain, drizzle) or solid (e.g. hail, snow), that fall from a cloud or group of clouds and reach the ground.</p>
                            <hr class="glossary-hr" />
                            <h5 class="text-info">Rain</h5>
                            <p class="font-family text-justify">In contrast to showers, rain is steadier and normally falls from stratiform (layer) cloud. Liquid water drops greater than 0.5 mm in diameter. Rain can range in intensity from light to very heavy. </p>
                            <hr class="glossary-hr" />
                            <h5 class="text-info">Showers</h5>
                            <p class="font-family text-justify">Usually begin and end suddenly. Relatively short-lived, but may last half an hour. Fall from cumulus clouds, often separated by blue sky. Showers may fall in patches rather than across the whole forecast area. Range in intensity from light to very heavy.</p>
                            <hr class="glossary-hr" />
                            <h5 class="text-info">Drizzle</h5>
                            <p class="font-family text-justify">Fairly uniform precipitation composed exclusively of very small water droplets (less than 0.5 mm in diameter) very close to one another.</p>
                            <hr class="glossary-hr" />
                            <h5 class="text-info">Thunderstorms</h5>
                            <p class="font-family text-justify">
                                Thunderstorms are one or more convective clouds in which electrical discharge can be seen as lightning and heard as thunder by a person on the earth's surface.
                                A severe thunderstorm produces one or more of :-
                                <ul>
                                    <li>hail at the ground with diameter of 2 cm or more;</li>
                                    <li>wind gusts at the ground of 90 km/h or more;</li>
                                    <li>tornadoes; or</li>
                                    <li>very heavy rain likely to cause flash flooding.</li>
                                </ul>
                            </p>
