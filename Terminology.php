                            <h5 class="text-info">Climate</h5>
                            <p class="font-family text-justify">
                                The atmospheric conditions for a long period of time, and generally refers to the normal or mean course of the weather. Includes the future expectation of long term weather, in the order of weeks, months or years ahead.
                            </p>
                            <hr class="glossary-hr" />
                            <h5 class="text-info">Monsoon</h5>
                            <p class="font-family text-justify">
                                Bangladesh is characterized by a tropical climate, i.e. 'cool', dry winter from November to January with predominantly northeasterly monsoon winds; hot, humid summer from April into October; and wet monsoon from June to September with predominantly southwesterly monsoon winds. Flooding in Bangladesh occurs during the wet monsoon period.
                            </p>
                            <hr class="glossary-hr" />
                            <h5 class="text-info">Green house effect</h5>
                            <p class="font-family text-justify">
                                The green house effect is a change in the global climate due to mans activities. The IPCC "Business-as-Usual emissions scenario" predicted water level rise forced by green house effect is about 20 cm in global mean sea level by 2030, and 65 cm by the end of next century. There will be significant regional variations. Also the climate of Bangladesh may get warmer and wetter. The monsoon rainfall is estimated to increase 10-15% by the year 2030. Little is known on changes in cyclone frequency and intensity. Presently FFWC does not investigate the consequences of the green house effect.
                            </p>
                            <hr class="glossary-hr" />
                            <h5 class="text-info">El Ni√±o</h5>
                            <p class="font-family text-justify">
                                An El Ni√±o/Southern Oscilation (ENSO) episode is a disruption of the ocean-atmosphere system in the tropical Pacific having important consequences for the weather and climate around the globe. Abnormally warm waters in the equatorial central and eastern Pacific characterize an ENSO episode. During ENSO episodes abnormally dry conditions over northern Australia, Indonesia and the Philippines are found, and the Indian monsoon rainfall tends to be less than normal. It is possible that ENSO episodes influence flood conditions in Bangladesh and research project are investigating the connection.
