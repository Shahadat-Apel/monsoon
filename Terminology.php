                            <h5 class="text-info">Flood</h5>
                            <p class="font-family text-justify">flood occurs when water inundates (covers) land which is normally dry.</p>
                            <hr class="glossary-hr" />
                            <h5 class="text-info">Flood in Bangladesh</h5>
                            <p class="font-family text-justify">The floods in Bangladesh are divided into monsoon river flood, flash flood, local rainfall flood and storm surge flood. Monsoon river flood is an annual event forced mainly by intensive river inflow through Ganges, Brahmaputra and Meghna Rivers and rainfall over Bangladesh as causes the water level in the rivers to rise and fall slowly during the monsoon season. Flash flood occurs only in the northeastern Bangladesh in the period pre- to post-monsoon forced by intense rainfall in the Meghalaya Hills and in parts of eastern Bangladesh in the post-monsoon. Local rainfall flood is, as the name states, forced by local heavy rainfall over a location inside Bangladesh. Storm surge flood is a coastal phenomenon forced by cyclones hitting the Bangladeshi coastline.</p>
                            <hr class="glossary-hr" />
                            <h5 class="text-info">Flash Flood</h5>
                            <p class="font-family text-justify">
                                Flood of short duration with a relatively high peak discharge.
                            </p>
                            <hr class="glossary-hr" />
                            <h5 class="text-info">Flood Forecasting</h5>
                            <p class="font-family text-justify">Estimation of height, discharge, time of occurrence, and duration of a flood, especially of peak discharge, at a specified point on a stream, resulting from precipitation and/or snowmelt.</p>
                            <hr class="glossary-hr" />
                            <h5 class="text-info">Flood Warning</h5>
                            <p class="font-family text-justify">Advance notice that a flood may occur in the near future at a certain location or in a certain river basin.</p>
                            <hr class="glossary-hr" />
                            <h5 class="text-info">Danger level</h5>
                            <p class="font-family text-justify">In Bangladesh danger level at a river location is the level above which it is likely that the flood may cause damages to nearby crops and homesteads. In a river having no embankment, danger level is about annual average flood level. In an embanked river, danger level is fixed slightly below design flood level of the embankment. The danger level at a given location needs continuous verification as e.g. embankments may be breached, but it is not done continuously by FFWC, whereby some danger levels may be not precise.</p>
                            <hr class="glossary-hr" />
                            <h5 class="text-info">Water level</h5>
                            <p class="font-family text-justify">BWDB and other government departments refer water levels to the Public Works Datum (PWD). PWD is a horizontal datum believed originally to have zero at a determined Mean Sea Level (MSL) at Calcutta. PWD is located approx. 1.5 ft below the MSL established in India under the British Rule and brought to Bangladesh during the Great Trigonometric Survey.</p>
